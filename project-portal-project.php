		<p>
			After thirty years of civil war that has left two and a half million people dead and four million displaced, South Sudan secured its independence in 2011 becoming 
			the World’s newest country. A more recent power struggle within the borders of South Sudan has killed tens of thousands more and displaced a further two million, 
			most of whom are young children. Children are the first victims in the aftermath of war, the first victims of disease and the first victims of famine.
		</p>
		
		<p>
			In the remote border village of Turalei, PEACE PALETTE, a community based non governmental organisation provides water, food, shelter and healthcare facilities to 
			the orphans of the Nhomlau Children’s Centre, a temporary shelter built to house the children on the street. The PORTAL PROJECT is providing a way for these children 
			and the greater village to both receive communications from the outside World and also have the ability to communicate and share information within their local 
			community more effectively and immediately. The PORTAL PROJECT is transporting a large projector screen, media player and audio system on the long and difficult 
			journey into the isolated northern area of South Sudan. The equipment will be set up within the children's shelter and will remain in place, acting as a tool for 
			educational programs, public meetings, ceremonies, broadcasting information, health bulletins, and some much needed entertainment on occasion.
		</p>
		
		<p>
			PEACE PALETTE was founded by David Vincent a former child soldier, who survived 18 years in refugee camps before being awarded an humanitarian visa to Australia, he 
			now devotes his life to helping the people of South Sudan by assisting them to determine their own future. David saw that the next step was reconnecting the children 
			with their wider community. It does take a village to raise a child, and it is the mission of the PORTAL PROJECT to reconnect the children of the orphanage with their 
			community currently spread across the globe. The PORTAL PROJECT is currently recording and compiling personal video messages of hope and support from the South Sudanese 
			community in Melbourne. These video messages are from people originally from the village of Turalei that have been separated by the civil war. They have all experienced 
			some of the difficulty these children are currently facing and aim to uplift their spirits and offer hope.
		</p>
		
		<p>
			These messages will be projected in the orphanage shelter along with the South Sudan premier of the wonderful Australian children’s film PAPER PLANES. This will also 
			act as an opening celebration and give insight into the Australian way of life. Following the screening of the video messages, the children currently living under the 
			shelter, along with other members of the community, will be invited to learn how to record each other on camera, interview one another, share their stories and record 
			their own video replies. For the orphans of Turalei it is an opportunity to have an active voice in what is often a one way relationship in receiving aid.
		</p>
		
		<p>
			In addition, video footage of the growth of the orphanage from a simple shelter to a cultural and educational hub will be documented along with a snapshot of the 
			progress of that region, including interviews with the highest levels of leadership to the most vulnerable members of the community. Through this we will capture 
			for both the Australian and South Sudanese communities the work that is being done during this difficult time in the early years of this very new country. These 
			recordings will also form the beginnings of a library archive to be based in Turalei. The PORTAL PROJECT aims to leave the orphans of a remote village in South 
			Sudan with both a tool for ongoing growth and education and a sense of connection with the World at large that is currently beyond reach. 
		</p>
		
