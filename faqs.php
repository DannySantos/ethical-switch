		<h4>
			<a id="where-will-my-power-actually-come-from">
				Where will my power actually come from?
			</a>
		</h4>
		
		<p>
			Electricity is energy, and the Australian grid is an ever changing mix of the energy supplied into it. This energy 
			can be locally sourced, renewable or from fossil fuel generators. Currently the National Energy Market (NEM) mix is 
			only ~13% renewable sourced, with the rest coming from fossil fuels. No matter which energy retailer you buy your 
			electricity from, the electricity is delivered via your distributor from the mix of energy present in your local 
			area, for ease this is measured by the Australian Energy Market Operator (AEMO) centrally, for each state, while 
			the NEM extends across the eastern seaboard from SA across Qld through to Tasmania. To help manage their business, 
			electricity retailers have a range of ways to source their electricity, including buying from the NEM or by sourcing 
			electricity directly from generators. The government mandates that a certain percentage of the electricity in the NEM 
			comes from renewable sources.  
		</p>
		
		<p>
			<span class="underline">Powershop</span>
			
			<br />
			
			Powershop buys its electricity, like all retailers, from the NEM. However, Powershop is backed by Meridian Energy which owns and runs the Mt Miller 
			wind farm, which is situated on the Eyre Peninsular in South Australia. Mt Miller wind farm produces around 180 GWh of renewable energy per year. 
			They also own the Mt Mercer wind farm near Ballarat which has just begun generating enough electricity to supply over 100,000 households. These wind 
			farms produce more renewable power than Powershop’s customers consume. By purchasing power from Powershop you are supporting Meridian Energy and its 
			investment in renewable energy generation in Australia.
		</p>
		
		<p>
			<span class="underline">Diamond</span>
			
			<br />
			
			Diamond Energy sources electricity from renewable generators only, including their own Tatura and Shepparton biogas 
			plants, two solar plants in Mildura, a food waste to energy facility in outer Sydney, wind in South Australia and 
			from thousands of residential solar customer rooftops. They believe that clean and efficient renewable generation is 
			going to play an increasing role in Australia’s future energy mix and a major role in helping combat climate change, 
			ahead of old and tired coal generation.
		</p>
		
		<p>
			Their vision is to ‘generate change’ as an electricity retailer supporting renewable generation derived from 
			Australia’s pure, natural resources.   They also believe that by supporting Diamond Energy, customers can take 
			a simple step to make their electricity spend count. Every day they are supporting renewable generation whilst 
			enjoying competitive rates and great service for their homes and businesses
		</p>
		
