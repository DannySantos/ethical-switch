				What is GreenPower?
			</a>
		</h4>
		
		<p>
			The GreenPower Program is a government managed scheme that enables Australian households and businesses to displace their electricity usage with 
			certified renewable energy, which is added to the grid on their behalf.
		</p>
		
		<p>
			With each of our switches, you may optionally select GreenPower and this will help increase the demand for renewable energy in Australia.  
		</p>
		
		<p>
			When you select GreenPower you are forcing your electricity provider to buy more certified renewable energy than government mandated via the RET 
			scheme.  This helps drive investment in renewable energy in Australia!
		</p>
		
