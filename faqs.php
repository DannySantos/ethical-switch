		<h4>
			<a id="what-is-the-renewable-energy-target-ret">
				What is the Renewable Energy Target (RET)?
			</a>
		</h4>
		
		<p>
			The Renewable Energy Target is a policy that encourages the development of renewable energy in Australia. It does this by requiring certain businesses, 
			mainly electricity retailers, to purchase a certain number of Renewable Energy Certificates (RECs). REC’s are created by renewable energy power 
			stations, such as wind farms, that are accredited by the Clean Energy Regulator.
		</p>
		
